#include "pse.igen.h"

#define PREFIX "USEC_COUNTER"


//////////////////////////////// Callback stubs ////////////////////////////////


PPM_REG_READ_CB(rdUSEC) {
    double now = bhmGetLocalTime();

    if(PSE_DIAG_HIGH) {
        bhmMessage("I", PREFIX, "Time now %10.4f uS", now);
    }

    return (Uns32) now;
}


