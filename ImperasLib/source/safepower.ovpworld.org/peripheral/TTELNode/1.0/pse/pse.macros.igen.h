#ifndef PSE_MACROS_IGEN_H
#define PSE_MACROS_IGEN_H               

// Before including this file in the application, define the indicated macros
// to fix the base address of each slave port.

// Set the macro 'HOSTIF' to the base of port 'hostif'
#ifndef HOSTIF
#error HOSTIF is undefined. It needs to be set to the port base address
#endif


#define HOSTIF_REC_POFFSET              0x0
#define HOSTIF_REC_PORT_POFFSET         0x0
#define HOSTIF_REC_PORT                 (HOSTIF + HOSTIF_REC_POFFSET + HOSTIF_REC_PORT_POFFSET)
#define HOSTIF_REC_PORT_BYTES           0x10000

#define HOSTIF_REC_TERM_POFFSET         0x8000
#define HOSTIF_REC_TERM                 (HOSTIF + HOSTIF_REC_POFFSET + HOSTIF_REC_TERM_POFFSET)
#define HOSTIF_REC_TERM_BYTES           0x1fff

#define HOSTIF_PORT_POFFSET             0x10000
#define HOSTIF_PORT_DATA_POFFSET        0x0
#define HOSTIF_PORT_DATA                (HOSTIF + HOSTIF_PORT_POFFSET + HOSTIF_PORT_DATA_POFFSET)
#define HOSTIF_PORT_DATA_BYTES          0x1fff

#define HOSTIF_PORT_LENGTH_POFFSET      0x4000
#define HOSTIF_PORT_LENGTH              (HOSTIF + HOSTIF_PORT_POFFSET + HOSTIF_PORT_LENGTH_POFFSET)
#define HOSTIF_PORT_LENGTH_BYTES        0x1fff

#define HOSTIF_PORT_TERM_POFFSET        0x8000
#define HOSTIF_PORT_TERM                (HOSTIF + HOSTIF_PORT_POFFSET + HOSTIF_PORT_TERM_POFFSET)
#define HOSTIF_PORT_TERM_BYTES          0x1fff





















































































































































































































































































































































































































































































































































































































































































































































































#define HOSTIF_ERR_POFFSET              0xfe0000
#define HOSTIF_ERR_ERR_POFFSET          0x0
#define HOSTIF_ERR_ERR                  (HOSTIF + HOSTIF_ERR_POFFSET + HOSTIF_ERR_ERR_POFFSET)
#define HOSTIF_ERR_ERR_BYTES            0x10000

#define HOSTIF_MON_POFFSET              0xff0000
#define HOSTIF_MON_EMPTY_POFFSET        0x0
#define HOSTIF_MON_EMPTY                (HOSTIF + HOSTIF_MON_POFFSET + HOSTIF_MON_EMPTY_POFFSET)
#define HOSTIF_MON_EMPTY_BYTES          0x20

#define HOSTIF_MON_FULL_POFFSET         0x20
#define HOSTIF_MON_FULL                 (HOSTIF + HOSTIF_MON_POFFSET + HOSTIF_MON_FULL_POFFSET)
#define HOSTIF_MON_FULL_BYTES           0x20

#define HOSTIF_MON_STATUS_POFFSET       0x40
#define HOSTIF_MON_STATUS               (HOSTIF + HOSTIF_MON_POFFSET + HOSTIF_MON_STATUS_POFFSET)
#define HOSTIF_MON_STATUS_BYTES         0x400

#define HOSTIF_MON_GBLTIM0_POFFSET      0x440
#define HOSTIF_MON_GBLTIM0              (HOSTIF + HOSTIF_MON_POFFSET + HOSTIF_MON_GBLTIM0_POFFSET)
#define HOSTIF_MON_GBLTIM0_BYTES        0x4

#define HOSTIF_MON_GBLTIM1_POFFSET      0x444
#define HOSTIF_MON_GBLTIM1              (HOSTIF + HOSTIF_MON_POFFSET + HOSTIF_MON_GBLTIM1_POFFSET)
#define HOSTIF_MON_GBLTIM1_BYTES        0x4


#endif
